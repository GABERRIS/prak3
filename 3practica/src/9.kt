fun main() {
    // Запрашиваем у пользователя два целых числа
    println("Введите первое число (m):")
    val m = readLine()!!.toInt()
    println("Введите второе число (n):")
    val n = readLine()!!.toInt()

    // Проверяем, делится ли m на n без остатка
    if (m % n == 0) {
        // Если делится, выводим сообщение
        println("$m делится нацело на $n")
    } else {
        // Если не делится, выводим другое сообщение
        println("$m не делится нацело на $n")
    }
}